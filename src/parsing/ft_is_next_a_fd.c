#include "parsing.h"

t_bool	ft_is_next_a_fd(char *str)
{
	int	i;

	i = 0;
	while (str[i] && str[i] >= '0' && str[i] <= '9')
		i++;
	if (str[i] == '>')
		return (TRUE);
	return (FALSE);
}
