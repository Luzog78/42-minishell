#include "builtins.h"

int	ft_env(char **argv, char **env)
{
	int	i;

	i = 0;
	if (argv[1] && argv[1][0] == '-')
		return (125);
	else if (argv[1] && argv[1][0] != '-')
		return (127);
	while (env[i])
	{
		printf("%s\n", env[i]);
		i++;
	}
	return (0);
}
