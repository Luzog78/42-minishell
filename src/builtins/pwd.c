#include "builtins.h"

int	ft_pwd(char **env)
{
	int	i;

	i = 0;
	while (env[i])
	{
		if (ft_strstr(env[i], "PWD="))
		{
			printf("%s\n", env[i] + 4);
			return (0);
		}
		i++;
	}
	return (1);
}
