#include "utils.h"

void	ft_strcat(char *dest, char *str)
{
	int	i;
	int	j;

	i = 0;
	while (dest[i])
		i++;
	j = 0;
	while (str[j])
	{
		dest[i + j] = str[j];
		j++;
	}
	dest[i + j] = 0;
}
